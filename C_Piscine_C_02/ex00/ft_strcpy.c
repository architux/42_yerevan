int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	dest_len;
	int	i;

	dest_len = ft_strlen(dest);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	while (i <= dest_len)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
